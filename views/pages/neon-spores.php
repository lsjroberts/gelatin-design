<h1>Neon Spores</h1>

<p>
	<small>Competition - <a href="http://www.ludumdare.com">Ludum Dare 24</a></small><br>
	<small>Source - <a href="http://github.com/gelatindesign/Ludum-Dare-24-Evolution">http://github.com/gelatindesign/Ludum-Dare-24-Evolution</a></small>
</p>

<p>Neon Spores is a game about helping your green plants evolve and spread faster than the nasty purple plants.</p>

<p>You can shoot your green spores to give them energy to grow faster, and shoot the purple guys to kill them and stop
	their spread. (<a href="https://raw.github.com/gelatindesign/Ludum-Dare-24-Evolution/master/how-to-play.png">How
	to play</a>)</p>

<p>As an entry for <a href="http://www.ludumdare.com">Ludum Dare</a> it was made in 48 hours.</p>


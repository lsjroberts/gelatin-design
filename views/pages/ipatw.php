<h1>i painted a tiny world</h1>

<p>
	<small>Competition - <a href="http://www.ludumdare.com">Ludum Dare 23</a></small><br>
	<small>Source - <a href="http://github.com/gelatindesign/Ludum-Dare-23">http://github.com/gelatindesign/Ludum-Dare-23</a></small>
</p>

<p>i painted a tiny world is a game about collecting tiny worlds with your vacuum and painting your own platforms to
	run on.</p>

<p>The paint gradually slides away and disappears forcing your to keep moving.</p>

<p>Your enemies are 'turpentine' blobs that fly past wiping away your paint.</p>

<p>As an entry for <a href="http://www.ludumdare.com">Ludum Dare</a> it was made in 48 hours.</p>

